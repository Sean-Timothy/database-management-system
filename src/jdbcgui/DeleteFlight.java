    private void populateFields() {
        try {
            txtFlightCode.setText(rs.getString("flightCode"));
            txtAirline.setText(rs.getString("Airlinename")); 
            txtCapacity.setText(rs.getString("capacity"));           
            txtBaseAirport.setText(rs.getString("baseairport"));
                      
            txtOccupied.setText(rscountno.getString("Count(seat#)"));
            //cmbFlight.setSelectedItem(rs.getString("flightcode"));       
           

            EnableDisableButtons();
        } catch (SQLException ex) {
            Logger.getLogger(DeleteFlight.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void MoveNext() {
        try {
            // TODO add your handling code here:
            if (!rs.isLast()) {
                rs.next();
                populateFields();
            }
        } catch (SQLException ex) {
            Logger.getLogger(DeleteFlight.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    private void btnNextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNextActionPerformed
        MoveNext();
    }//GEN-LAST:event_btnNextActionPerformed

    private void MovePrevious() {
        try {
            // TODO add your handling code here:
            if (!rs.isFirst()) {
                rs.previous();
                populateFields();
            }
        } catch (SQLException ex) {
            Logger.getLogger(DeleteFlight.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void EnableDisableButtons() {
        try {
            if (rs.isFirst()) {
                btnPrevious.setEnabled(false);
            } else {
                btnPrevious.setEnabled(true);
            }
            if (rs.isLast()) {
                btnNext.setEnabled(false);
            } else {
                btnNext.setEnabled(true);
            }
        } catch (SQLException ex) {
            Logger.getLogger(DeleteFlight.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    private void btnPreviousActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPreviousActionPerformed
        // TODO add your handling code here:
        MovePrevious();
    }//GEN-LAST:event_btnPreviousActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        //CONFIRM DELETE MESSAGE;
        int responseDel=JOptionPane.showConfirmDialog(this,"Do you want to Delete this record?", "confirm", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        //IF RESULT IS YES, DELETE, otherwise nothing.
        if (responseDel==JOptionPane.YES_OPTION){
            try {
            // make the result set scrolable forward/backward updatable
            String prepSQL = "DELETE FLIGHT WHERE flightcode = '"+ txtFlightCode.getText().trim()+"';";
            int result = dbCon.executePrepared(prepSQL);
            if (result > 0) {
                javax.swing.JLabel label = new javax.swing.JLabel("Flight Code " + txtFlightCode.getText().trim() + " deleted successfully.");
                label.setFont(new java.awt.Font("Arial", java.awt.Font.BOLD, 18));
                JOptionPane.showMessageDialog(null, label, "SUCCESS", JOptionPane.INFORMATION_MESSAGE);
                getNewData();
            }
        }
            catch (SQLException e) {
    JOptionPane.showMessageDialog(null, "Error deleting new Flight: " + e.getMessage());
    e.printStackTrace();
            
        } 
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

