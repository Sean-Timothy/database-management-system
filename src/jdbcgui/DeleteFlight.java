    private void getNewData() {

        try {
            
            // populate mgr field
            rs = dbCon.executeStatement("SELECT flightcode, airlinename, capacity, baseairport FROM flight");
            
            rscountno= dbCon.executeStatement("SELECT COUNT(seat#)FROM BOOKING_RESERVATION");  
            // populate rest of fields
            rs.beforeFirst();
            rs.first();
            rscountno.beforeFirst();
            rscountno.first();
            populateFields();
        } catch (SQLException e) {
            javax.swing.JLabel label = new javax.swing.JLabel("SQL Error - Display selected empno.");
            label.setFont(new java.awt.Font("Arial", java.awt.Font.BOLD, 18));
            JOptionPane.showMessageDialog(null, label, "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }

